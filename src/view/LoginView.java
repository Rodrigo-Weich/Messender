    private void auth_level() {
        Map<String, Object> filtro = new HashMap<>();
        filtro.put("usuLogin", "root");
        filtro.put("usuSenha", "imroot");
        
        Usuario mainroot = (Usuario) DaoGenerico.getInstance().buscarUnicoWhereAnd(Usuario.class, filtro);
        
        if (mainroot == null) {
            Endereco end = new Endereco();
            end.setEndBairro("Santo Antônio");
            end.setEndCid("Planalto");
            end.setEndCod(null);
            end.setEndComp("Próximo ao posto de saúde");
            end.setEndLog("Casa");
            end.setEndNum(94);
            
            List<Usuario> usur = new ArrayList<>();
            
            Calendar dateNasc = new GregorianCalendar(1998,5,12);
            
            Usuario root = new Usuario();
            root.setUsuCad(Calendar.getInstance());
            root.setUsuCod(null);
            root.setUsuEmail(null);
            root.setUsuFrase(null);
            root.setUsuLogin("root");
            root.setUsuNasc(dateNasc);
            root.setUsuNome("Root");
            root.setUsuResp(null);
            root.setUsuSenha("imroot");
            root.setUsuEnd(end);
            usur.add(root);
            
            end.setUsuarios(usur);

            DaoGenerico.getInstance().inserir(end);
        }
    }

