    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String user = jtLogin.getText();
        String password = String.valueOf(jpfSenha.getPassword());
        
        Map<String, Object> filtro = new HashMap<>();
        filtro.put("usuLogin", user);
        filtro.put("usuSenha", password);
        
        Usuario login = (Usuario) DaoGenerico.getInstance().buscarUnicoWhereAnd(Usuario.class, filtro);

        if ("".equals(user)) {
            JOptionPane.showMessageDialog(null, "O campo Usuário não pode estar vazio!", null, JOptionPane.WARNING_MESSAGE);
            jtLogin.requestFocus();
        } else if ("".equals(password)) {
            JOptionPane.showMessageDialog(null, "O campo Senha não pode estar vazio!", null, JOptionPane.WARNING_MESSAGE);
            jpfSenha.requestFocus();
        } else {
            if (login == null) {
                JOptionPane.showMessageDialog(null, "Usuário ou senha incorretos!.", null, JOptionPane.ERROR_MESSAGE);
                jtLogin.requestFocus();
            } else {
                System.setProperty("ID", login.getUsuCod().toString());
                UserView u = new UserView();
                u.setVisible(true);
                this.dispose();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

