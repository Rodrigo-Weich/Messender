    private void jbCurtirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCurtirActionPerformed
        // TODO add your handling code here:
        Map<String, Object> filtros = new HashMap<>();
        curtida = new Curtidas();
        filtros.put("curUsu", logado);
        filtros.put("curPost", pos);
        Curtidas curt = (Curtidas) DaoGenerico.getInstance().buscarUnicoWhereAnd(Curtidas.class, filtros);
        if (curt == null) {

            curtida.setCurCod(null);
            curtida.setCurData(Calendar.getInstance());
            curtida.setCurUsu(logado);
            curtida.setCurPost(pos);
            DaoGenerico.getInstance().inserir(curtida);
            JOptionPane.showMessageDialog(null, "Post curtido com sucesso");
        } else {
            JOptionPane.showMessageDialog(null, "Você já curtiu esse post!");
        }
        carregarTabelaCurtidas();
    }//GEN-LAST:event_jbCurtirActionPerformed

    private void jbDescurtirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbDescurtirActionPerformed
        // TODO add your handling code here:
        Map<String, Object> filtros = new HashMap<>();
        filtros.put("curUsu", logado);
        filtros.put("curPost", pos);
        Curtidas curt = (Curtidas) DaoGenerico.getInstance().buscarUnicoWhereAnd(Curtidas.class, filtros);
        if (curt != null) {

            DaoGenerico.getInstance().Deletar(curt);
            JOptionPane.showMessageDialog(null, "Post descurtido com sucesso");
        } else {
            JOptionPane.showMessageDialog(null, "Você não curtiu esse post!");
        }
        carregarTabelaCurtidas();
    }//GEN-LAST:event_jbDescurtirActionPerformed

    private void jbComentarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbComentarActionPerformed
        // TODO add your handling code here:
        try {
            Comentarios com = new Comentarios();
            com.setComCod(null);
            com.setComText(jtComentarios.getText());
            com.setComData(Calendar.getInstance());
            com.setComUsu(logado);
            com.setComPost(pos);
            DaoGenerico.getInstance().inserir(com);
            JOptionPane.showMessageDialog(null, "Inserido com sucesso");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro ao inserir " + e );
        }finally{
            jtComentarios.setText("");
            jtComentarios.requestFocus();
            carregarTabelaComentarios();
        }
    }//GEN-LAST:event_jbComentarActionPerformed

