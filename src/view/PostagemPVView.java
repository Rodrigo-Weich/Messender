    private void carregarTabelaComentarios() {
        jTabComentarios.setVisible(false);
        tblModelCom.getDataVector().clear();
        comentarios = (List<Comentarios>) DaoGenerico.getInstance().BuscarTodosWhere(Comentarios.class, "comPost", pos);
        SimpleDateFormat sdfd = new SimpleDateFormat("dd/MM/yyyy");
        SimpleDateFormat sdft = new SimpleDateFormat("HH:mm.ss");
        
        try {
            for (Comentarios com : comentarios) {
                tblModelCom.addRow(new Object[]{
                    com.getComUsu().getUsuNome(),
                    com.getComText(),
                    sdfd.format(com.getComData().getTime()) + " as " + sdft.format(com.getComData().getTime())
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
        }finally{
            jTabComentarios.setModel(tblModelCom);
            jTabComentarios.setVisible(true);
        }
    }

    public void carregarTabelaCurtidas() {
        jTabCurtidas.setVisible(false);
        tblModelC.getDataVector().clear();
        PC = (List<Curtidas>) DaoGenerico.getInstance().BuscarTodosWhere(Curtidas.class, "curPost", pos);
        SimpleDateFormat sdfd = new SimpleDateFormat("dd/MM/yyyy");
        SimpleDateFormat sdft = new SimpleDateFormat("HH:mm.ss");
        try {
            for (Curtidas like : PC) {
                tblModelC.addRow(new Object[]{
                    like.getCurUsu().getUsuNome(),
                    sdfd.format(like.getCurData().getTime()) + " as " + sdft.format(like.getCurData().getTime())
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            jTabCurtidas.setModel(tblModelC);
            jTabCurtidas.setVisible(true);
        }
    }

